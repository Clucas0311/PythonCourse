# Distance = speed * time

car_speed = 70
# The distance the car is traveling in 6 hours
car_1 = int(car_speed * 6)
print(f"Car one will travel at a distance {car_1} miles")

# The distance the car is traveling in 10 hours
car_2 = int(car_speed * 10)
print(f"Car two will travel at a distance {car_2} miles")

# The distance the car is traveling in 15 hours
car_3 = int(car_speed * 15)
print(f"Car three will travel at a distance {car_3} miles")
